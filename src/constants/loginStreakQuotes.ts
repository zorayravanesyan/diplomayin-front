export const LOGIN_STREAK_QUOTE_FALLBACK = 'Սկսիր այսօր 🔥';

function normalizeStreakCount(streakCount: unknown): number | null {
  if (streakCount === null || streakCount === undefined) return null;
  const n = typeof streakCount === 'number' ? streakCount : Number(streakCount);
  if (!Number.isFinite(n)) return null;
  return n;
}

/**
 * Վերադարձնում է օրվա շարքին համապատասխան մեջբերում։
 */
export function getStreakQuote(streakCount: number | null | undefined): string {
  const n = normalizeStreakCount(streakCount);
  if (n === null) return LOGIN_STREAK_QUOTE_FALLBACK;

  const day = Math.floor(n);
  if (day <= 0) return LOGIN_STREAK_QUOTE_FALLBACK;
  if (day <= 100) return LOGIN_STREAK_QUOTES[day] ?? LOGIN_STREAK_QUOTE_FALLBACK;
  return `${LOGIN_STREAK_QUOTES[100]} + շարունակում ես 🔥`;
}
